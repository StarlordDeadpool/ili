                        <section>
                            <h3 id="library_sections">Library Sections</h3>
                            <p style="text-indent: 30px;">Nueva Ecija University of Science and Technology Libraries Collection is organized on  the basis of convenience and easy access to all its clientele whether students, faculty or plain  researcher the University library accommodates and to whom its services are intended for.</p>
                            <p style="text-indent: 30px;">Types of collections is one basic considerations in its organization i.e all general  references are grouped and housed together like encyclopedia, dictionaries, directories,  manuals and handbook, yearbooks, at lasses etc. books reserved by faculty member for their  class assignment are also lumped together section, all books classified as Filipiniana can be  found together in another section, the same with all the multimedia collections, general  collections, etc. </p>
                            <ul>
                                <li>
                                    <p class="library-section-title"><b>CIRCULATION SECTION</b></p>
                                    <p style="text-indent: 30px;">This section contains the bulk of library collection. It houses the collection of books in  computer science,history, psychology, social sciences, engineering, nursing, education, and  other allied fields. </p>
                                    <p style="text-indent: 30px;">Textbooks, supplementary reading books, self-education books and fiction books are also  found in this section. </p>
                                    <p style="text-indent: 30px;">Students may borrow up to three circulation books at a time for 3 days depending on the  demand of the books, while faculty or staff members may borrow 5 circulation books at a time  for 7 days. </p>
                                </li>
                                <li>
                                    <p class="library-section-title"><b>FILIPINIANA SECTION</b></p>
                                    <p style="text-indent: 30px;">This section houses a collection of publications both in English or Filipino language or  even other spoken dialects about the Philippines regardless of author and imprint. </p>
                                    <p style="text-indent: 30px;">Filipiniana books shall be used in the library only. However, they can be borrowed overnight  and during weekends.</p>
                                    <p style="text-indent: 30px;">A library user may borrow one (1) book at a time depending on the demand of the books. Overnight loans must be returned on or before 9:00 A.M. of the following service day. </p>
                                </li>
                                <li>
                                    <p class="library-section-title"><b>PERIODICAL SECTION</b></p>
                                    <p style="text-indent: 30px;">The periodical section is the place where local and international journals, magazines,  newspaper and other serial materials can be found and borrowed. </p>
                                    <p style="text-indent: 30px;">Because of their nature, this material shall be preferably for “library use” only and may be  borrowed for photocopying purposes.</p>
                                </li>

                                <li>
                                    <p class="library-section-title"><b>REFERENCE SECTION</b></p>
                                    <p style="text-indent: 30px;">Materials found in this section are for use inside the Library only. They include  encyclopedias, dictionaries, handbooks, almanacs, yearbooks, atlases, gazettes, biographies  and indices.</p>
                                </li>

                                <li>
                                    <p class="library-section-title"><b>THESES/DISSERTATION SECTION</b></p>
                                    <p style="text-indent: 30px;">The theses section contains the theses, dissertations written by the NEUST Masteral and  Doctoral graduate students and undergraduate theses submitted to NEUST.</p>
                                </li>

                                <li>
                                    <p class="library-section-title"><b>E-LIBRARY SECTION</b></p>
                                    <p style="text-indent: 30px;">The Library maintains a computer room for students to access the worldwide web. The  computers with Internet access allow students to search online subject areas assigned by their  professor/teachers. </p>
                                    <p style="text-indent: 30px;">Internet user must ask permission from the library staff before using external storage devices. Only relevant information related to academic and research work shall be downloaded. </p>
                                    <p style="text-indent: 30px;"> Computer games and access to pornographic or offensive materials shall not be allowed</p>
                                </li>

                                <li>
                                    <p class="library-section-title"><b>REFERENCE SECTION</b></p>
                                    <p style="text-indent: 30px;">Where dictionaries, encyclopedias, yearbooks, handbooks, manuals, gazetteers, etc. are  housed. </p>
                                </li>

                                <li>
                                    <p class="library-section-title"><b>SPECIAL SECTION</b></p>
                                    <p style="text-indent: 30px;">It contains collections from generous donors like University of the Philippines Diliman.  Library and Bangko Sentral ng Pilipinas, among others. </p>
                                </li>

                                <li>
                                    <p class="library-section-title"><b>AUDIO-VISUAL MATERIALS SECTION</b></p>
                                    <p style="text-indent: 30px;">Found in this section for the use of students are videotapes, computer discs,  audiocassettes, slides and kits, interactive instructional CD-ROMs, and colored transparencies  on different subjects. </p>
                                    <p style="text-indent: 30px;">Audiovisual and electronic media materials as well as needed equipment may be borrowed for  classroom and training purposes. </p>
                                </li>

                                <li>
                                    <p class="library-section-title"><b>NEUST RESOURCE SECTION</b></p>
                                    <p style="text-indent: 30px;">The library collection of different pages or articles all about the university such as  Annual reports, journals, codes, yearbooks, modules and the likes.</p>
                                </li>
                                <li>
                                    <p class="library-section-title"><b>TECHNICAL AND STACK SECTION</b></p>
                                    <p style="text-indent: 30px;">Here, acquisitions work together with the cataloging and classification of books  are being done. It also maintains the following library records: ISO documents, accreditation documents, new acquisition of books, accession record, lists of processed  books transferred to the library sections and inventory record. </p>
                                </li>

                            </ul>
